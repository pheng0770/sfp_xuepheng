# List of names
names = ['Tom', 'Jerry', 'Butch', 'Tuffy', 'Pecos']

# 1. Print the whole list
print(names)

# 2. Print the first name (index 0)
print(names[0])

# 3. Print the last name (index -1 means last item)
print(names[-1])

# 4. Print the middle two names: 'Butch' and 'Tuffy'
print(names[2:4])